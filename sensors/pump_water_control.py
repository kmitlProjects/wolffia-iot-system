from gpiozero import OutputDevice
import time

# ---------------------------------------------------------
# 1. ตั้งค่าขา GPIO
# ---------------------------------------------------------
# pump_1 = OutputDevice(5) 
pump_2 = OutputDevice(6) 

def run_pumps(run_time_seconds):
    print(f"กำลังเปิดปั๊มทั้ง 2 ตัว พร้อมกัน เป็นเวลา {run_time_seconds} วินาที...")
    
    # ---------------------------------------------------------
    # 2. สั่งปั๊มทำงานพร้อมกัน (100% เต็มกำลัง)
    # ---------------------------------------------------------
    # pump_1.on()
    pump_2.on()
    
    # ปล่อยให้ปั๊มทำงานตามเวลาที่กำหนด
    time.sleep(run_time_seconds)
    
    # ---------------------------------------------------------
    # 3. สั่งปิดปั๊มพร้อมกัน
    # ---------------------------------------------------------
    # pump_1.off()
    pump_2.off()
    print("ปิดปั๊มน้ำเรียบร้อยแล้ว!")

# --- เริ่มการทำงาน ---
if __name__ == "__main__":
    try:
        run_pumps(10)
    except KeyboardInterrupt:
        # ถ้ากด Ctrl+C ให้ปิดปั๊มทันทีเพื่อความปลอดภัย
        # pump_1.off()
        pump_2.off()
        print("\nหยุดการทำงานฉุกเฉิน")
